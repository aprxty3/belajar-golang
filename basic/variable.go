package main

import "fmt"

func main() {
	var name string

	name = "Aji"
	fmt.Println(name)

	name = "Aji Prasetyo"
	fmt.Println(name)

	var number = 38
	fmt.Println(number)

	var (
		firstName  = "Aji"
		lastName   = "Prasetyo"
		numberInit = 37
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
	fmt.Println(numberInit)
}
